double numero, resposta;
int calculo;
    
public void operacao(){
    
    switch(calculo){
        case 1:
            resposta = numero+Double.parseDouble(jTextField1.getText());
            jTextField1.setText(Double.toString(resposta));
            break;
            
        case 2:
            resposta = numero - Double.parseDouble(jTextField1.getText());
            jTextField1.setText(Double.toString(resposta));
            break;
            
        case 3:
            resposta = numero * Double.parseDouble(jTextField1.getText());
            jTextField1.setText(Double.toString(resposta));
            break;
            
        case 4:
            resposta = numero / Double.parseDouble(jTextField1.getText());
            jTextField1.setText(Double.toString(resposta));
            break;
            
        case 5:
            resposta = (numero/100)* Double.parseDouble(jTextField1.getText());
            jTextField1.setText(Double.toString(resposta));
            break;
            
        case 6:
            resposta = (Math.pow(numero, Double.parseDouble(jTextField1.getText())));
            jTextField1.setText(Double.toString(resposta));
            break;
        case 7:
            resposta = Double.parseDouble(jTextField1.getText())+(Math.sqrt(numero));
            jTextField1.setText(Double.toString(resposta));
            break;
            
        case 8:
            resposta = Math.log10(Double.parseDouble(jTextField1.getText()));
            jTextField1.setText(Double.toString(resposta));
    }
}
    
