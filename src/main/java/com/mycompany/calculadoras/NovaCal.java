    public void operacao(){
        
        switch(calculo){
            case 1 -> {
                resposta = numero + Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(resposta));
            }
                
            case 2 -> {
                resposta = numero - Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(resposta));
            }
                
            case 3 -> {
                resposta = numero * Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(resposta));
            }
                
            case 4 -> {
                resposta = numero / Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(resposta));
            }
        }
    }
